<?php

namespace Bbdgnc\Test\Smiles\Parser;

use Bbdgnc\Finder\Enum\ServerEnum;
use Bbdgnc\Smiles\Parser\Accept;
use Bbdgnc\Smiles\Parser\PdbParser;
use PHPUnit\Framework\TestCase;

final class PdbParserTest extends TestCase {

    public function testWithNull() {
        $parser = new PdbParser();
        $this->assertEquals(PdbParser::reject(), $parser->parse(null));
    }

    public function testWithEmptyString() {
        $parser = new PdbParser();
        $this->assertEquals(PdbParser::reject(), $parser->parse(''));
    }

    public function testWithRightData() {
        $parser = new PdbParser();
        $this->assertEquals(new Accept(ServerEnum::PDB, ''), $parser->parse('PDB: '));
    }

    public function testWithRightData2() {
        $parser = new PdbParser();
        $this->assertEquals(new Accept(ServerEnum::PDB, '1554418'), $parser->parse('PDB: 1554418'));
    }

    public function testWithWrongData() {
        $parser = new PdbParser();
        $this->assertEquals(PdbParser::reject(), $parser->parse('CID: '));
    }

    public function testWithWrongData2() {
        $parser = new PdbParser();
        $this->assertEquals(PdbParser::reject(), $parser->parse('CID:'));
    }

    public function testWithWrongData3() {
        $parser = new PdbParser();
        $this->assertEquals(PdbParser::reject(), $parser->parse('5'));
    }

    public function testWithWrongData4() {
        $parser = new PdbParser();
        $this->assertEquals(PdbParser::reject(), $parser->parse('CID'));
    }

}
